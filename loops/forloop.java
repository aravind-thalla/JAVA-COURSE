public class forloop {

  public static void main(String[] args) {
    for(int i=0; i<5; i++){
      System.out.println("hello world");
    }
    // print 0 to 20
    for(int i=0; i<21; i++){
      System.out.println(i);
    }
    // to print in one line
    for(int i=0; i<21; i++){
      System.out.print(i + " ");
    }
  }
}